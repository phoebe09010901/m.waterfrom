                        <span class="font_02">Humorous Art</span><br />
                        <span class="font_03">因為一直對設計保持高度好奇心，對於太制式的殻體會忍不住皺眉。設計的養分不僅來自偉大建築師，更多來自藝術家，我們試圖把幽默玩心放在空間中，變成一只大型裝置藝術。</span>
